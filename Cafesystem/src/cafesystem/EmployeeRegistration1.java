    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Connection conn = null;
        PreparedStatement pst = null;

        try {

            String sql = "Insert into  employee(Employee_ID,Employee_Responsibility,First_Name,Last_Name,Sex,DateOfBirth,Salary,UserName,Password, EmpRole) values(?,?,?,?,?,?,?,?,?,?)";

            conn = ConnectToDB.getConnected();
            pst = conn.prepareStatement(sql);
            java.util.Date dobPicked = txtdatchuser.getDate();
            //convert date to MySQL date format
            String datePattern = "yyyy-MM-dd";
            SimpleDateFormat dateFormatter = new SimpleDateFormat(datePattern);
            String dobStr = dateFormatter.format(dobPicked);

            try {
                pst.setString(1, txtEmployeeid.getText());
                pst.setString(2, cmbRole.getSelectedItem().toString());
                pst.setString(3, txtfirstname.getText());

                pst.setString(4, txtlastname.getText());

                pst.setString(5, cmbsex.getSelectedItem().toString());
                pst.setString(6, dobStr);
                pst.setDouble(7, Double.parseDouble(txtsalary.getText()));
                pst.setString(8, txtUsername.getText());
                pst.setString(9, txtPassword.getText());
                pst.setInt(10, cmbRole.getSelectedIndex());

            } catch (Exception e) {
            }

            int a = pst.executeUpdate();
            if (a > 0) {
                JOptionPane.showMessageDialog(this, "Record saved Successfully.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database server error" + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

